package basicjava;

public class Concatenation {

	public static void main(String[] args) {
		String var1 = "Hello";
		String var2 = "World";
		System.out.println(var1 + " " + var2);
		System.out.println(var1 + " " + 1234);
		System.out.println(var2 + " " + 2 + 1 + "0" + 5 * 10);
		System.out.println(0 + 1 + "0" + 5 * 10);

	}

}
